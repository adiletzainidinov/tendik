// Dev-time validation
function validateProgram() {
  const allLessons = PROGRAM_MODULES.flatMap((m) => m.lessons);
  if (allLessons.length !== 80) {
    throw new Error(
      `Program must have exactly 80 lessons, got ${allLessons.length}`,
    );
  }
  const numbers = allLessons.map((l) => l.number);
  for (let i = 1; i <= 80; i++) {
    if (!numbers.includes(i)) {
      throw new Error(`Missing lesson number ${i}`);
    }
  }
  const unique = new Set(numbers);
  if (unique.size !== 80) {
    throw new Error("Duplicate lesson numbers detected");
  }
  for (const mod of PROGRAM_MODULES) {
    const [start, end] = mod.lessonRange.split("–").map(Number);
    const lessonNums = mod.lessons.map((l) => l.number);
    if (lessonNums[0] !== start || lessonNums[lessonNums.length - 1] !== end) {
      throw new Error(
        `Module ${mod.romanNumber} lessonRange ${mod.lessonRange} doesn't match lessons ${lessonNums[0]}–${lessonNums[lessonNums.length - 1]}`,
      );
    }
  }
  const totalPoints = ASSESSMENT_CATEGORIES.reduce(
    (sum, c) => sum + c.points,
    0,
  );
  if (totalPoints !== 100) {
    throw new Error(`Assessment total must be 100, got ${totalPoints}`);
  }
}

validateProgram();
